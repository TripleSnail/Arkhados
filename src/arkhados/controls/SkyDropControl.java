/**
 *
 * @author william
 */
public class SkyDropControl extends AbstractControl {

    private EntityAction groundCollisionAction;
    private boolean collided = false;

    public void onGroundCollision() {
        if (!this.collided) {
            this.collided = true;
            this.spatial.getControl(ActionQueueControl.class).enqueueAction(this.groundCollisionAction);

        }
    }

    @Override
    protected void controlUpdate(float tpf) {
    }

    @Override
    protected void controlRender(RenderManager rm, ViewPort vp) {
    }

    @Override
    public Control cloneForSpatial(Spatial spatial) {
        SkyDropControl control = new SkyDropControl();
        return control;
    }

    @Override
    public void read(JmeImporter im) throws IOException {
        super.read(im);
        InputCapsule in = im.getCapsule(this);
    }

    @Override
    public void write(JmeExporter ex) throws IOException {
        super.write(ex);
        OutputCapsule out = ex.getCapsule(this);
    }

    public void setGroundCollisionAction(EntityAction groundCollisionAction) {
        this.groundCollisionAction = groundCollisionAction;
    }
}
